public class Arithmetic{
    public static void main (String [] args){
        double a = 3;
        double b = 4;
        double add = a+b;
        double diff = a-b;
        double mul = a*b;
        double div = a/b;
        double modulo = a%b;
        System.out.println(add);
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(modulo);
    }
}